// Нормализация массива
export function normalize(arr, invert = false) {
	const min = Math.min(...arr)
	const max = Math.max(...arr)
	if (max === min) return arr.map(() => 1)
	return arr.map(v => {
		let norm = (v - min) / (max - min)
		if (invert) norm = 1 - norm
		return norm
	})
}
